    private void butLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butLoginActionPerformed
        connectServer();
        if (tfUserName.getText().equals("") || tfPassword.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Hãy nhập mật khẩu và tài khoản của bạn để đăng nhập !!");
            return;
        }
        if (listenServer != null) {
            User user = new User(tfUserName.getText(), tfPassword.getText());
            DataPackage data = new DataPackage(new User(), "requestLogin", new Gson().toJson(user, User.class));
            listenServer.SendMessage(data);
        }
    }//GEN-LAST:event_butLoginActionPerformed

