    void connectServer() {
        IP = "127.0.0.1";
        Port = "8889";
        if (socket == null) {
            try {
                socket = new Socket(IP, Integer.parseInt(Port));
                listenServer = new ListenServer(socket);
                listenServer.receive = this;
                listenServer.start();
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }

    }

