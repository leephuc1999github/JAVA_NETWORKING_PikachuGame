    private void butLoginKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_butLoginKeyPressed
        if (evt.getKeyCode() == 13) {
            connectServer();
            if (tfUserName.getText().equals("") || tfPassword.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Hãy nhập mật khẩu và tài khoản của bạn để đăng nhập !!");
                return;
            }
            if (listenServer != null) {
                User user = new User(tfUserName.getText(), tfPassword.getText());
                DataPackage data = new DataPackage(new User(), "requestLogin", new Gson().toJson(user, User.class));
                listenServer.SendMessage(data);
            }
        }
    }//GEN-LAST:event_butLoginKeyPressed

