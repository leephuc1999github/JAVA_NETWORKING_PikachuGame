    @Override
    public void ReceiveMessage(DataPackage dataPackage) throws IOException {
        user = (User) new Gson().fromJson(dataPackage.getValue(), User.class);
        if (user.getId() != 0) {
            java.awt.EventQueue.invokeLater(new Runnable() {
                public void run() {
                    HomeClientView homeClientView = new HomeClientView(listenServer);
                    homeClientView.setVisible(true);
                }
            });
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "đăng nhập thất bại");
        }

    }
