    public void loadHistory(ArrayList<String> history) {
        String[][] data = new String[history.size()][3];
        Pattern pattern = Pattern.compile("-");
        int j = 0;
        for (int i = 0; i < history.size(); i += 2) {
            String[] contain = pattern.split(history.get(i));
            String name = contain[0];
            int score = Integer.parseInt(contain[1]);
            int gameid = Integer.parseInt(contain[2]);
            String[] contain1 = pattern.split(history.get(i+1));
            String name1 = contain1[0];
            int score1 = Integer.parseInt(contain1[1]);
            int gameid1 = Integer.parseInt(contain1[2]);
            data[j][0] = name;
            data[j][1] = score + "-" + score1;
            data[j][2] = name1;
            j++;
        }
        DefaultTableModel defaultTableModel = new DefaultTableModel(data, new String[]{"Name", "", "Name Guest"});
        jTableHistory.setModel(defaultTableModel);
    }

